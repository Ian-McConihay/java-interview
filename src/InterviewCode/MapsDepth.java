package InterviewCode;

import java.util.HashMap;

public class MapsDepth {
	public static void main(String[] args) {

		HashMap<String, Integer> empIds = new HashMap<String, Integer>();
		empIds.put("Ian", 123);
		empIds.put("John", 321);
		System.out.println(empIds.get("Ian"));
		System.out.println(empIds.containsKey("Ian"));
		System.out.println(empIds.containsValue(123));
		System.out.println(empIds);








//Maps are just a collection of key value pairs
	}
}
